package io.github.vanm0012.symphonymanager.util;

public class Date
{
    private String date;

    public Date() {}

    public Date(String date)
    {
        this.date = date;
    }

    public void setDate(String date)
    {
        this.date = date;
    }

    public String getDate()
    {
        return date;
    }
}
